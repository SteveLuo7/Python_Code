# Original list
unsorted_list = [4, 2, 7, 1, 9, 3]

# Sorting the list in ascending order
sorted_list_ascending = sorted(unsorted_list)
print("Sorted in ascending order:", sorted_list_ascending)

# Sorting the list in descending order
sorted_list_descending = sorted(unsorted_list, reverse=True)
print("Sorted in descending order:", sorted_list_descending)

